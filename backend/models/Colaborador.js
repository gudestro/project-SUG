class Colaborador {
    constructor(nome, cargo_id, cpf_cnpj, telefone, email, rua, bairro, numero, cidade, cep) {
        this.nome = nome;
        this.cargo_id = cargo_id;
        this.cpf_cnpj = cpf_cnpj;
        this.telefone = telefone;
        this.email = email;
        this.rua = rua;
        this.bairro = bairro;
        this.numero = numero;
        this.cidade = cidade;
        this.cep = cep;
    }
}
export default Colaborador;